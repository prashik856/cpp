/*
The gray code is a binary numeral system where two successive values differ in only one bit.

Given a non-negative integer n representing the total number of bits in the code, print the sequence of gray code. A gray code sequence must begin with 0.

For example, given n = 2, return [0,1,3,2]. Its gray code sequence is:

00 - 0
01 - 1
11 - 3
10 - 2
There might be multiple gray code sequences possible for a given n.

Return any such sequence.
*/
/*
Solution Approach:
Gray code is calculated this way
Given a decimal number with n bits in position x,(x+1),(x+2)... from left to right,
The gray code will be:
x, x ^ (x+1), (x+1) ^ (x + 2), (x + 2) ^ (x + 3), .... and so on.
We see that, each number is xoring with the next number to get the gray code.
so if we take a number and right shift it,
we will bring previous bit position for given bit in current number
We can use it to our advantage.
We find max value given n, that will be 2 ^ n.
Start i from 0 to 2^n - 1, calculate j as i/2
and take i ^ j. This will be our required gray code.
*/
