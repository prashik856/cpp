class ListNode{
    public: 
        int val;
        ListNode *next;
        ListNode(int x) : val(x), next(NULL) {}
};

ListNode* createLinkedList(vector<int> arr){
    ListNode *head = 0;
    int n = arr.size();
    for(int i=0; i<n; i++){
        int value = arr[i];
        ListNode *newNode = new ListNode(value);

        if(head == 0){
            head = newNode;
        } else {
            ListNode *temp = head;
            while(temp->next != 0){
                temp = temp -> next;
            }
            temp -> next = newNode;
        }
    }
    return head;
}

void printLinkedList(ListNode *head){
    ListNode *temp = head;
    while(temp != 0){
        cout << temp->val << " ";
        temp = temp -> next;
    }
    cout << endl;
}


int getLinkedListLength(ListNode *head){
    ListNode *temp = head;
    int count = 0;
    while(temp !=0){
        temp = temp -> next;
        count++;
    }
    return count;
}

class TreeNode{
    public:
        int val;
        TreeNode *left;
        TreeNode *right;
        TreeNode(int x): val(x), left(0), right(0){}
};

TreeNode *createTree(int rootValue, vector<int> a){
    TreeNode *root = new TreeNode(rootValue);

    // Create Tree
    for(int i=0; i<a.size(); i++){
        int val = a[i];
        TreeNode *node = new TreeNode(val);
        // We need to find the location for this val
        TreeNode *temp = root;
        while(true){
            // if value is less than current root
            if(val <= temp -> val){
                if(temp -> left == 0){
                    // found it
                    temp -> left = node;
                    break;
                } else {
                    temp = temp -> left;
                    continue;
                }
            } 
            // if value is greater than current root
            else {
                if(temp -> right == 0){
                    // found it
                    temp -> right = node;
                    break;
                } else {
                    temp = temp -> right;
                    continue;
                }
            }
        }
    }
    return root;
}

// Depth first search
void preOrder(TreeNode *root){
    if(root == 0){
        return;
    }
    cout << root -> val << " ";
    preOrder(root -> left);
    preOrder(root -> right);
}

// Sorted order
void inOrder(TreeNode *root){
    if(root == 0){
        return;
    }
    inOrder(root -> left);
    cout << root -> val << " ";
    inOrder(root -> right);
}

// Visited all children first
void postOrder(TreeNode *root){
    if(root == 0){
        return;
    }
    postOrder(root -> left);
    postOrder(root -> right);
    cout << root -> val << " ";
}

// Breadth first search
void bfs(TreeNode *root){
    if(root == 0){
        return;
    }
    queue<TreeNode *> q;
    q.push(root);

    while(!q.empty()){
        TreeNode *node = q.front();
        q.pop();
        cout << node->val << " ";
        if(node -> left != 0){
            q.push(node -> left);
        }
        if(node -> right != 0){
            q.push(node -> right);
        }
    }
}

class DisjointSet {
    private:
        vector<int> parent;
        vector<int> rank;
        int n;
    public:
        DisjointSet(int N){
            parent.clear();
            rank.clear();
            n = N;
            // parent of i is i
            for(int i=0; i<n; i++){
                parent.push_back(i);
            }
            // rank is 0?
            for(int i=0; i<n; i++){
                rank.push_back(0);
            }
        }

        int find(int i){
            if(parent[i] == i){
                return i;
            }

            else {
                // since parent[i] != i, we find representative of parent[i]
                int result = find(parent[i]);
                
                // Cache value
                parent[i] = result;
                
                return result;
            }
        }

        void getUnion(int i, int j) {
            /*
            1. If the rank of left is less than the rank of right, then it’s best to move left under right, 
            because that won’t change the rank of right (while moving right under left would increase the height). 
            2. In the same way, if the rank of right is less than the rank of left, then we should move right under left.
            3. If the ranks are equal, it doesn’t matter which tree goes under the other, but the rank of the result will 
            always be one greater than the rank of the trees.
            */
            // get root of i
            int irep = find(i);

            // get root of j
            int jrep = find(j);

            // if irep == jrep, we have same root
            if(irep == jrep){
                return;
            }

            // Else, let's get their ranks
            int rankirep = rank[irep];
            int rankjrep = rank[jrep];

            if(rankirep == rankjrep){
                // set anything
                parent[irep] = jrep;
                // put i under j
                // increase rank of j
                rank[jrep]++;
            } else if(rankirep > rankjrep) {
                // we move j under i, and we don't have to increase the rank of i
                parent[jrep] = irep;
            } else {
                // rankjrep > rankirep
                // we move i under j, and we don't have to increase the rank of j
                parent[irep] = jrep;
            }
        }
};

// Depth first search using Stack
void dfs(TreeNode *root){
    stack<TreeNode *> s;
    s.push(root);
    while(!s.empty()){
        TreeNode *node = s.top();
        s.pop();
        cout << node -> val << " ";
        if(node -> left != 0){
            s.push(node -> left);
        }
        if(node -> right != 0){
            s.push(node -> right);
        }
    }
}

bool solve2(string &a, string &b, int ia, int ib, string &c, string &cs, vector< vector<int> > &table) {
    // ia - indexA
    // ib - indexB
    // cs - currentString
    if(cs[cs.size() - 1] == c[cs.size() - 1]) {
        // base cases
        if(ia >= a.size() && ib >= b.size()){
            // everything is equal now
            return true;
        }

        int cache = table[ia][ib];
        if(cache != -1){
            if(cache == 0){
                return false;
            } else {
                return true;
            }
        }

        // we can push b
        if(ia >= a.size()){
            cs.push_back(b[ib]);
            bool result1 = solve2(a, b, ia, ib + 1, c, cs, table);
            cs.pop_back();
            table[ia][ib] = result1;
            return result1;
        }

        // we can push a
        if(ib >= b.size()){
            cs.push_back(a[ia]);
            bool result1 = solve2(a, b, ia + 1, ib, c, cs, table);
            cs.pop_back();
            table[ia][ib] = result1;
            return result1;
        }
        
        cs.push_back(a[ia]);
        bool result1 = solve2(a, b, ia + 1, ib, c, cs, table);
        cs.pop_back();
        cs.push_back(b[ib]);
        bool result2 = solve2(a, b, ia, ib + 1, c, cs, table);
        cs.pop_back();
        bool tempResult = result1 || result2;
        table[ia][ib] = tempResult;
        return tempResult;
    } else {
        // added char is not equal, we would like to return
        return false;
    }
}

int solve(string A, string B, string C) {
    int result = 0;

    if(A.size() + B.size() != C.size()) {
        return result;
    }

    if(A.size() == 0 && B.size() == 0){
        return 1;
    }

    vector< vector<int> > table(A.size() + 1, vector<int> (B.size() + 1, -1));

    string cs;
    bool m1 = false;
    if(A.size() > 0){
        cs.push_back(A[0]);
        m1 = solve2(A, B, 1, 0, C, cs, table);
        cs.pop_back();
    }
    bool m2 = false;
    if(B.size() > 0){
        cs.push_back(B[0]);
        m2 = solve2(A, B, 0, 1, C, cs, table);
        cs.pop_back();
    }
    bool m = m1 || m2;
    if(m){
        result = 1;
    }
    return result;
}

