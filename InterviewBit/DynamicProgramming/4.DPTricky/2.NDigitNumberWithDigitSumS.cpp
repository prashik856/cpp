/*
Find out the number of N digit numbers, whose digits on being added equals to a given number S. Note that a valid number starts from digits 1-9 except the number 0 itself. i.e. leading zeroes are not allowed.

Since the answer can be large, output answer modulo 1000000007

N = 2, S = 4 
 Valid numbers are {22, 31, 13, 40} 
 Hence output 4.

Solution Approach:
https://www.geeksforgeeks.org/count-of-n-digit-numbers-whose-sum-of-digits-equals-to-given-sum/
    countRec(n, sum) = ∑countRec(n-1, sum-x)
                            where 0 =< x = 0

One important observation is, leading 0's must be handled explicitly as they are not counted as digits.
So our final count can be written as below.    
finalCount(n, sum) = ∑countRec(n-1, sum-x) where 1 =< x = 0

Yes, this works.
*/

